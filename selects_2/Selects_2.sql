SELECT * FROM CLIENTE 
INNER JOIN PROVEEDOR 
ON CLIENTE.DIR = PROVEEDOR.DIR_PRO;
SELECT NOM_PRO FROM PROVEEDOR
INNER JOIN PRODUCTO
ON PROVEEDOR.RFC_PRO != PRODUCTO.NOMP

SELECT * FROM CLIENTE 
LEFT JOIN PROVEEDOR 
ON CLIENTE.DIR = PROVEEDOR.DIR_PRO;
SELECT * FROM COMPRA 
LEFT JOIN CLIENTE
ON COMPRA.CURP = CLIENTE.CURP;

SELECT * FROM CLIENTE 
RIGHT JOIN PROVEEDOR 
ON CLIENTE.DIR = PROVEEDOR.DIR_PRO;
SELECT * FROM COMPRA 
RIGHT JOIN PRODUCTO
ON COMPRA.COD_P = PRODUCTO.COD_P;


